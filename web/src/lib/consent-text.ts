import type { ConsentId } from '../store/types'

export interface ConsentSpec {
  id: ConsentId
  /** Block label A / B / D / T / S (C omitted; T = Terms; S = Signature/access grant) */
  block: 'A' | 'B' | 'D' | 'T' | 'S'
  required: boolean
  /** Title rendered on the summary card and the modal header */
  title: string
  /** Short summary visible on the screen card */
  summary: string
  /** Full legal text shown inside the modal — scrollable, scroll-to-end gated */
  fullText: string
  /** Semantic version of the text — bump on any wording change */
  version: string
  /** True = optional consent, ack via direct tick (no scroll-to-end gate) */
  directTick: boolean
  /** True = the acknowledgement must be confirmed by an SMS one-time code. */
  requiresSmsConfirmation?: boolean
  /**
   * True = this block is the clinic access grant. The modal renders the
   * recipient card + scope + control sections + a ПЭП sign affordance instead
   * of a plain read-and-acknowledge. Acceptance is wired to `accessSigned` in
   * the store, not to a checkbox.
   */
  isAccessGrant?: boolean
  /** Optional partner-clinic identity for `isAccessGrant` blocks. */
  partner?: {
    initial: string
    shortName: string
    fullName: string
    role: string
    note: string
  }
  /** What the recipient clinic will see. Used by the access-grant modal. */
  scopeItems?: { iconId: 'file' | 'sparkles' | 'paperclip' | 'user'; text: string }[]
  /** What stays under the patient's control. Used by the access-grant modal. */
  controlItems?: { iconId: 'rotate' | 'lock' | 'shield'; text: string }[]
}

